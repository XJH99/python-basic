"""
python的常用数据类型：int，float，boolean，str，list，tuple，set，dict
检测数据类型方法：type()
"""
num1 = 1
print(type(num1))

num2 = 2.8
print(type(num2))

num3 = True
print(type(num3))

num4 = 'hello'
print(type(num4))

num5 = [10, 20, 30]
print(type(num5))

num6 = (10, 20)
print(type(num6))

num7 = {10, 20, 30}
print(type(num7))

num8 = {'name': 'jack', 'age': 20}
print(type(num8))
